%% Local function works because this file is run as a script file.
function writeCellFile(target,L)
    fid = fopen(target,'w');
    if fid < 0
        error('Could not open target for writing: %s', target);
    end
    for ii = 1:numel(L)
        fprintf(fid,'%s\n',L{ii});
    end
    fclose(fid);
end

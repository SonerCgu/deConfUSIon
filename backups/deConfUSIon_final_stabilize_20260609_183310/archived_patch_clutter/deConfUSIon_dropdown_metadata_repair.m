%% deConfUSIon dropdown/metadata repair - script-file version
% Fixes missing dropdown/data-assignment helper functions after cleanup.
%
% Usage:
%   run('D:\Github\HUMOR-Analysis-Tool\deConfUSIon_dropdown_metadata_repair.m')
%
% IMPORTANT:
%   Run this as a .m script file, not pasted line-by-line into Command Window.

clearvars -except root
clc

if exist('root','var') ~= 1 || isempty(root)
    root = 'D:\Github\HUMOR-Analysis-Tool';
end
root = char(regexprep(root,'[\\/]+$',''));

if exist(root,'dir') ~= 7
    error('Toolbox root not found: %s', root);
end

stamp = datestr(now,'yyyymmdd_HHMMSS');
repairDir = fullfile(root,'backups',['deConfUSIon_dropdown_metadata_repair_' stamp]);
if exist(repairDir,'dir') ~= 7
    mkdir(repairDir);
end

fprintf('\n=== deConfUSIon dropdown/metadata repair ===\n');
fprintf('ROOT: %s\n', root);
fprintf('BACKUP: %s\n\n', repairDir);

filesToBackup = { ...
    'deConfUSIon_fix_studio_dataset_names.m', ...
    'deConfUSIon_write_full_display_metadata.m', ...
    'deConfUSIon_commit_full_display_name.m', ...
    'deConfUSIon_best_visible_dataset_name.m', ...
    'deConfUSIon_is_bad_display_name.m', ...
    'deConfUSIon_display_from_file_context.m' ...
};

for i = 1:numel(filesToBackup)
    f = fullfile(root,filesToBackup{i});
    if exist(f,'file') == 2
        copyfile(f, fullfile(repairDir,filesToBackup{i}));
        fprintf('Backed up existing: %s\n', filesToBackup{i});
    end
end

%% 1) deConfUSIon_is_bad_display_name.m
writeCellFile(fullfile(root,'deConfUSIon_is_bad_display_name.m'), { ...
'function tf = deConfUSIon_is_bad_display_name(s)' ...
'% True if visible name looks like an internal temporary/preproc key.' ...
'tf = false;' ...
'if nargin < 1 || isempty(s), tf = true; return; end' ...
'try, s = char(s); catch, tf = true; return; end' ...
'low = lower(s);' ...
'bad = {''preproc_preproc'',''filter_filter'',''pca_pca'',''ica_ica'',''imreg_imreg'',''motor_motor''};' ...
'for i = 1:numel(bad)' ...
'    if ~isempty(strfind(low,bad{i})), tf = true; return; end' ...
'end' ...
'if ~isempty(strfind(s,''...'')), tf = true; return; end' ...
'if ~isempty(regexp(low,''^preproc_[0-9a-f]{6,}'',''once'')), tf = true; return; end' ...
'end' ...
});

%% 2) deConfUSIon_display_from_file_context.m
writeCellFile(fullfile(root,'deConfUSIon_display_from_file_context.m'), { ...
'function displayName = deConfUSIon_display_from_file_context(matFile, fallbackStem)' ...
'% Build readable name for saved/lazy preprocessing files when metadata is missing.' ...
'if nargin < 1, matFile = ''''; end' ...
'if nargin < 2 || isempty(fallbackStem), fallbackStem = ''''; end' ...
'try, matFile = char(matFile); catch, matFile = ''''; end' ...
'try, fallbackStem = char(fallbackStem); catch, fallbackStem = ''''; end' ...
'[folder,stem] = fileparts(matFile);' ...
'if isempty(fallbackStem), fallbackStem = stem; end' ...
'combo = [fallbackStem ''_'' folder];' ...
'low = lower(combo);' ...
'parts = {};' ...
'an = regexp(combo,''B6J[_-]?(\d{3,6})'',''tokens'',''once'');' ...
'if isempty(an), an = regexp(combo,''[_-](\d{3,6})[_-]Session'',''tokens'',''once''); end' ...
'if isempty(an), an = regexp(combo,''[_-](\d{3,6})[_-]scan'',''tokens'',''once''); end' ...
'if ~isempty(an), parts{end+1} = an{1}; end' ...
'sess = regexp(folder,''Session[_-]?0*([0-9]+)'',''tokens'',''once'');' ...
'if ~isempty(sess), parts{end+1} = sprintf(''sess%03d'',str2double(sess{1})); end' ...
'sl = regexp(combo,''Slice0*([0-9]+)of0*([0-9]+)'',''tokens'',''once'');' ...
'if isempty(sl), sl = regexp(combo,''sl0*([0-9]+)of0*([0-9]+)'',''tokens'',''once''); end' ...
'if ~isempty(sl), parts{end+1} = sprintf(''sl%03dof%03d'',str2double(sl{1}),str2double(sl{2})); end' ...
'ops = {};' ...
'if ~isempty(strfind(low,''splitmotor'')) || ~isempty(strfind(low,''_motor'')), ops{end+1} = ''motor''; end' ...
'tok = regexp(fallbackStem,''imreg[^_]*_?(med|median|mean)?_?n\d+'',''match'',''once'');' ...
'if ~isempty(tok), ops{end+1} = regexprep(tok,''median'',''med'',''ignorecase''); end' ...
'tok = regexp(fallbackStem,''BPF[^_]*to[^_]*Hz_o\d+|LPF[^_]*Hz_o\d+|HPF[^_]*Hz_o\d+'',''match'',''once'');' ...
'if ~isempty(tok), ops{end+1} = tok; end' ...
'tok = regexp(fallbackStem,''pca[^_]*_?dropPC[^_]*|dropPC[^_]*'',''match'',''once'');' ...
'if ~isempty(tok)' ...
'    if isempty(strfind(lower(tok),''pca'')), tok = [''pca_'' tok]; end' ...
'    ops{end+1} = tok;' ...
'end' ...
'tok = regexp(fallbackStem,''ica[^_]*_?dropIC[^_]*|dropIC[^_]*'',''match'',''once'');' ...
'if ~isempty(tok)' ...
'    if isempty(strfind(lower(tok),''ica'')), tok = [''ica_'' tok]; end' ...
'    ops{end+1} = tok;' ...
'end' ...
'if isempty(parts), base = ''dataset''; else, base = strjoin(parts,''_''); end' ...
'if isempty(ops)' ...
'    if ~isempty(strfind(lower(folder),''preprocessing'')) || ~isempty(strfind(lower(folder),''splitmotor''))' ...
'        ops{end+1} = ''processed'';' ...
'    else' ...
'        ops{end+1} = ''raw'';' ...
'    end' ...
'end' ...
'ts = regexp(fallbackStem,''\d{8}_\d{6}'',''match'',''once'');' ...
'displayName = strjoin([{base} ops],''_'');' ...
'if ~isempty(ts), displayName = [displayName ''_'' ts]; end' ...
'displayName = regexprep(displayName,''_+'',''_'');' ...
'displayName = regexprep(displayName,''^_+|_+$'','''');' ...
'end' ...
});

%% 3) deConfUSIon_best_visible_dataset_name.m
writeCellFile(fullfile(root,'deConfUSIon_best_visible_dataset_name.m'), { ...
'function nameOut = deConfUSIon_best_visible_dataset_name(seedName, dataStruct, matFile)' ...
'% Robust visible dataset name for Studio dropdowns.' ...
'if nargin < 1 || isempty(seedName), seedName = ''dataset''; end' ...
'if nargin < 2, dataStruct = []; end' ...
'if nargin < 3, matFile = ''''; end' ...
'try, seedName = char(seedName); catch, seedName = ''dataset''; end' ...
'try, matFile = char(matFile); catch, matFile = ''''; end' ...
'candidates = {};' ...
'if isstruct(dataStruct)' ...
'    flds = {''HUMOR_fullDisplayName'',''displayNameFull'',''preprocDisplayName'',''fullDisplayName'',''sourceDisplayName'',''sourceDatasetName''};' ...
'    for i = 1:numel(flds)' ...
'        if isfield(dataStruct,flds{i}) && ~isempty(dataStruct.(flds{i}))' ...
'            try, candidates{end+1} = char(dataStruct.(flds{i})); catch, end %#ok<AGROW>' ...
'        end' ...
'    end' ...
'end' ...
'candidates{end+1} = seedName;' ...
'if ~isempty(matFile)' ...
'    [~,stem] = fileparts(matFile);' ...
'    candidates{end+1} = stem;' ...
'end' ...
'nameOut = '''';' ...
'for i = 1:numel(candidates)' ...
'    s = localClean(candidates{i});' ...
'    if ~deConfUSIon_is_bad_display_name(s)' ...
'        nameOut = s;' ...
'        break;' ...
'    end' ...
'end' ...
'if isempty(nameOut)' ...
'    if ~isempty(matFile)' ...
'        nameOut = deConfUSIon_display_from_file_context(matFile, seedName);' ...
'    else' ...
'        nameOut = localClean(seedName);' ...
'    end' ...
'end' ...
'nameOut = localClean(nameOut);' ...
'if isempty(nameOut), nameOut = ''dataset''; end' ...
'end' ...
'function s = localClean(s)' ...
'try, s = char(s); catch, s = ''dataset''; end' ...
's = regexprep(s,''\.mat$'','''',''ignorecase'');' ...
's = strrep(s,''...'',''_'');' ...
's = regexprep(s,''_+'',''_'');' ...
's = regexprep(s,''^_+|_+$'','''');' ...
'end' ...
});

%% 4) deConfUSIon_commit_full_display_name.m
writeCellFile(fullfile(root,'deConfUSIon_commit_full_display_name.m'), { ...
'function fullName = deConfUSIon_commit_full_display_name(matFile, dataStruct, fallbackName)' ...
'% Append exact full display name into top-level MAT metadata.' ...
'if nargin < 1, matFile = ''''; end' ...
'if nargin < 2, dataStruct = []; end' ...
'if nargin < 3 || isempty(fallbackName), fallbackName = ''dataset''; end' ...

% ---------------------------------------------------------------------
% Nested callbacks
% ---------------------------------------------------------------------
    function toggleTrim(~,~)
        if get(cbTrim,'Value') == 1
            set(editStart,'Enable','on');
            set(editEnd,'Enable','on');
            set(statusTxt,'String','Trimming enabled. Enter start/end seconds.');
        else
            set(editStart,'Enable','off');
            set(editEnd,'Enable','off');
            set(statusTxt,'String','No trimming selected.');
        end
    end

    function onRecommended(~,~)
        set(methodGroup,'SelectedObject',rbDVARS);
        set(interpGroup,'SelectedObject',rbLinear);
        set(cbTrim,'Value',0);
        set(editStart,'Enable','off','String','30');
        set(editEnd,'Enable','off','String','30');
        set(statusTxt,'String','Recommended defaults restored: DVARS + Linear + No trimming.');
    end

    function onRun(~,~)

        detObj = get(methodGroup,'SelectedObject');
        intObj = get(interpGroup,'SelectedObject');

        if isempty(detObj) || isempty(intObj)
            set(statusTxt,'ForegroundColor',[1.00 0.35 0.35], ...
                'String','Please select detection and interpolation method.');
            return;
        end

        doTrimNow = get(cbTrim,'Value') == 1;

        startSec = 0;
        endSec   = 0;

        if doTrimNow
            startSec = str2double(get(editStart,'String'));
            endSec   = str2double(get(editEnd,'String'));

            if ~isfinite(startSec) || startSec < 0
                set(statusTxt,'ForegroundColor',[1.00 0.35 0.35], ...
                    'String','Invalid START trim. Enter a number >= 0.');
                return;
            end

            if ~isfinite(endSec) || endSec < 0
                set(statusTxt,'ForegroundColor',[1.00 0.35 0.35], ...
                    'String','Invalid END trim. Enter a number >= 0.');
                return;
            end
        end

        cfg.cancelled    = false;
        cfg.method       = get(detObj,'UserData');
        cfg.interpMethod = get(intObj,'UserData');
        cfg.doTrim       = doTrimNow;
        cfg.trimStartSec = startSec;
        cfg.trimEndSec   = endSec;

        if ishandle(dlg)
            uiresume(dlg);
        end
    end

    function onCancel(~,~)
        cfg.cancelled = true;
        if ishandle(dlg)
            uiresume(dlg);
        end
    end

